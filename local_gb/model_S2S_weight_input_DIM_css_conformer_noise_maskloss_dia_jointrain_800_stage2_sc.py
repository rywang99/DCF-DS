    def perturb_mask_torch(self, masks, label_data, perturbation_probability=0.5, zeroing_fraction_range=(0.1, 0.3)):
        """
        Perturbs the given speaker masks using PyTorch tensors, ensuring the zeroed and one-set segments are continuous.
        
        :param masks: Torch tensor of shape (batch_size, speaker_number, frame_number)
        :param perturbation_probability: Probability of perturbing a given speaker's mask
        :param zeroing_fraction_range: Tuple indicating the range of fraction of active parts to zero out
        :return: Perturbed masks
        """
        batch_size, speaker_number, frame_number = masks.shape
        perturbed_masks = masks.clone()

        for i in range(batch_size):
            for j in range(speaker_number):
                if random.random() < perturbation_probability:
                    active_indices = torch.where(label_data[i, j] == 1)[0]
                    non_active_indices = torch.where(label_data[i, j] == 0)[0]

                    # Determine the number of active indices to zero out
                    zeroing_fraction = random.uniform(*zeroing_fraction_range)
                    num_to_zero = int(len(active_indices) * zeroing_fraction)
                    
                    # Determine a random length to reassign, up to the number zeroed
                    num_to_reassign = random.randint(0, num_to_zero)
                    print("num_to_zero: {}, num_to_reassign: {}".format(num_to_zero, num_to_reassign))
                    # pdb.set_trace()
                    # Ensure continuous segment for zeroing
                    if len(active_indices) >= num_to_zero:
                        start_zero = random.randint(0, len(active_indices) - num_to_zero)
                        zero_indices = active_indices[start_zero:start_zero + num_to_zero]
                        perturbed_masks[i, j, zero_indices] = 0
                    
                    # Ensure a continuous segment for reassigning original values
                    if len(non_active_indices) > num_to_reassign:
                        # print("num_to_reassign: ", num_to_reassign)
                        start_one = random.randint(0, len(non_active_indices) - num_to_reassign)
                        reassign_indices = non_active_indices[start_one:start_one + num_to_reassign]
                        perturbed_masks[i, j, reassign_indices] = masks[i, j, zero_indices[:num_to_reassign]]

        return perturbed_masks
    
