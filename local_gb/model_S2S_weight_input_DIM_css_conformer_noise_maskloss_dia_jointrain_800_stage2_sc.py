class DecoderBlock(nn.Module):
    def __init__(self, attn_dim ,num_heads, ffn_num_hiddens,
                 mlp_num_input, mlp_num_hiddens, num_outputs, attn_dropout=0., dropout=0., **kwargs):
        super(DecoderBlock, self).__init__(**kwargs)

        self.pos_encoding = PositionalEncoding(num_outputs, dropout)

        #for speaker embeding
        self.num_hiddens = num_outputs
        self.dec_spkemb_weights_1 = nn.Parameter(torch.ones(2) / 2)
        self.dec_spkemb_weights_2 = nn.Parameter(torch.ones(2) / 2)

        self.attn_spkemb_weights = nn.Parameter(torch.ones(2) / 2)


        self.dropout = nn.Dropout(dropout)
        self.mlp = MLP(mlp_num_input, mlp_num_hiddens, num_outputs)
        self.qcontent_proj_1 = nn.Linear(num_outputs, num_outputs)
        self.qpos_proj_1 = nn.Linear(num_outputs, num_outputs) # in DAB-DERT, every component of concat is projed
        self.kcontent_proj_1 = nn.Linear(num_outputs, num_outputs)
        self.kpos_proj_1  = nn.Linear(num_outputs, num_outputs)
        self.v_proj_1 = nn.Linear(num_outputs, num_outputs)
        self.Pre_LN_1_1 = nn.LayerNorm(num_outputs)
        self.Pre_LN_1_2 = nn.LayerNorm(num_outputs)
        self.Pre_LN_1_3 = nn.LayerNorm(num_outputs)
        self.attention1 = MultiheadAttention(attn_dim, num_heads, attn_dropout, vdim=num_outputs)

        
        self.qcontent_proj_2 = nn.Linear(num_outputs, num_outputs)
        self.qpos_proj_2 = nn.Linear(num_outputs, num_outputs)
        self.kcontent_proj_2 = nn.Linear(num_outputs, num_outputs)
        self.v_proj_2 = nn.Linear(num_outputs, num_outputs)   

        self.Pre_LN_2_1 = nn.LayerNorm(num_outputs)
        self.Pre_LN_2_2 = nn.LayerNorm(num_outputs)
        self.Pre_LN_2_3 = nn.LayerNorm(num_outputs)
        self.attention2 = MultiheadAttention(attn_dim, num_heads, attn_dropout, vdim=num_outputs)
        
        self.ffn = FFN(num_outputs, ffn_num_hiddens, num_outputs)
       
                            

    def forward(self, decoder_embeddings, speaker_embeddings, encoder_embeddings):

        speaker_embeddings_ND = self.mlp(speaker_embeddings)
       
        qcontent_1 = self.qcontent_proj_1(decoder_embeddings)
        kcontent_1 = self.kcontent_proj_1(decoder_embeddings)
        q_pos_1 = self.qpos_proj_1(speaker_embeddings_ND)
        k_pos_1 = self.kpos_proj_1(speaker_embeddings_ND)
        
        
        dec_spkemb_norm_weights_1 = nn.functional.softmax(self.dec_spkemb_weights_1, dim=-1)
        dec_spkemb_1 = torch.stack([qcontent_1, q_pos_1], dim=1)
        Query_1 = (dec_spkemb_1 * dec_spkemb_norm_weights_1.view(-1, 1, 1)).sum(dim=1)

    
        dec_spkemb_norm_weights_2 = nn.functional.softmax(self.dec_spkemb_weights_2, dim=-1)
        dec_spkemb_2 = torch.stack([kcontent_1, k_pos_1], dim=1)
        Key_1 = (dec_spkemb_2 * dec_spkemb_norm_weights_2.view(-1, 1, 1)).sum(dim=1)


        Value_1 = self.v_proj_1(decoder_embeddings)

        Value_1 = self.Pre_LN_1_1(Value_1)
        Key_1 = self.Pre_LN_1_2(Key_1)
        Query_1 = self.Pre_LN_1_3(Query_1)   
        
        Query_1, Key_1, Value_1 = Query_1.transpose(0, 1), Key_1.transpose(0, 1), Value_1.transpose(0, 1)
        x_1, _ = self.attention1(Query_1, Key_1, Value_1)  
        x_1 = x_1.transpose(0, 1)
        x_1 = decoder_embeddings + self.dropout(x_1)    
                    
        
        qcontent_2 = self.qcontent_proj_2(x_1)
        kcontent_2 = self.kcontent_proj_2(encoder_embeddings)
        q_pos_2 = self.qpos_proj_2(speaker_embeddings_ND)
        
        attn_spkemb_norm_weights = nn.functional.softmax(self.attn_spkemb_weights, dim=-1)
        attn_spkemb = torch.stack([qcontent_2, q_pos_2], dim=1)
        Query_2 = (attn_spkemb * attn_spkemb_norm_weights.view(-1, 1, 1)).sum(dim=1)
        
        
        Key_2 = self.pos_encoding(kcontent_2 * math.sqrt(self.num_hiddens))

        
        Value_2 = self.v_proj_2(encoder_embeddings)
        Value_2 = self.Pre_LN_2_1(Value_2)
        Key_2 = self.Pre_LN_2_2(Key_2)
        Query_2 = self.Pre_LN_2_3(Query_2)
        Query_2, Key_2, Value_2 = Query_2.transpose(0, 1), Key_2.transpose(0, 1), Value_2.transpose(0, 1)                         
        x_3, _ = self.attention2(Query_2, Key_2, Value_2)  
        x_3 = x_3.transpose(0, 1)
        x_3 = x_1 + self.dropout(x_3)         
        out = self.ffn(x_3)
        
        return out
                            
class Decoder(nn.Module):                           
    def __init__(self, attn_dim ,num_heads, ffn_num_hiddens,
        mlp_num_input, mlp_num_hiddens, num_outputs, num_layers, attn_dropout=0., dropout=0., decode_Time=800, **kwargs):
        super(Decoder, self).__init__(**kwargs)
        self.blks = nn.Sequential()
        # pdb.set_trace()
        for i in range(num_layers):
            self.blks.add_module("block_"+str(i),
                DecoderBlock(attn_dim ,num_heads, ffn_num_hiddens,
                mlp_num_input, mlp_num_hiddens, num_outputs, attn_dropout, dropout))
        self.output_layer = nn.Linear(num_outputs,  decode_Time)
        
    def forward(self, decoder_embeddings, speaker_embeddings, encoder_embeddings):
        embedding_outlist = []
        for blk in self.blks:
            decoder_embeddings = blk(decoder_embeddings, speaker_embeddings, encoder_embeddings)
            embedding_outlist.append(decoder_embeddings)
            # pdb.set_trace()
        out = self.output_layer(decoder_embeddings)
        # pdb.set_trace()
        return out, embedding_outlist

